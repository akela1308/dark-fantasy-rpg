export class WalkableZones {
  constructor(mapKey) {
    this.zones = ZONES[mapKey] || [];
  }

  isWalkable(x, y) {
    return this.zones.some(z =>
      x >= z.x && x <= z.x + z.w &&
      y >= z.y && y <= z.y + z.h
    );
  }

  clamp(x, y) {
    if (this.isWalkable(x, y)) return { x, y };

    let best = null;
    let bestDist = Infinity;

    this.zones.forEach(z => {
      const cx = Math.max(z.x, Math.min(z.x + z.w, x));
      const cy = Math.max(z.y, Math.min(z.y + z.h, y));
      const d  = Math.hypot(cx - x, cy - y);
      if (d < bestDist) { bestDist = d; best = { x: cx, y: cy }; }
    });

    return best ?? { x, y };
  }

  /** Нарисовать зоны поверх карты для отладки */
  drawDebug(scene) {
    const g = scene.add.graphics().setDepth(100).setAlpha(0.45);
    g.lineStyle(2, 0x00FF44, 1);
    g.fillStyle(0x00FF44, 0.12);
    this.zones.forEach(z => {
      g.fillRect(z.x, z.y, z.w, z.h);
      g.strokeRect(z.x, z.y, z.w, z.h);
    });
  }
}
